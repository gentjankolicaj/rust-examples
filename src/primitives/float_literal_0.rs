fn main() {
    let float_literal = 3.14;
    println!("float_literal: {}", float_literal);

    let literal_double_suffixed = 3.14f64;
    println!("literal_double_suffixed: {}", literal_double_suffixed);

    let literal_underscore = 1_000_000.1456f64;
    println!("literal_underscore: {}", literal_underscore);
}
