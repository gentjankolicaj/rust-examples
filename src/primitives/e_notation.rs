fn main() {

    //1000
    let one_k = 1e3;
    println!("one_k: {}", one_k);

    //1_000_000
    let one_m = 1e6;
    println!("one_m: {}", one_m);

    //1/1000
    let one_one_k = 1e-3;
    println!("one_one_k: {}", one_one_k);

    //1/1_000_000
    let one_one_m = 1e-6;
    println!("one_one_m: {}", one_one_m);
}